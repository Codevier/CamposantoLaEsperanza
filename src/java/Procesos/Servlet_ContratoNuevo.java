            String nombre_user=(String) request.getSession().getAttribute("nom_user");
            String cedula_cliente= request.getParameter("cedula");
            String nombre_cliente= request.getParameter("nombre");
            String propiedad= request.getParameter("propiedad");
            String tipo= request.getParameter("tipo");
            String puerta= request.getParameter("puerta");
            String bloque= request.getParameter("bloque");
            String cara= request.getParameter("cara");
            RequestDispatcher dispatcher = request.getRequestDispatcher("/Admin_contratos_agg.jsp");
